package com.example.BusTrack.dto;

public class Veiculo {

    private String p;
    private double px;
    private double py;

    public Veiculo() {

    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public double getPx() {
        return px;
    }

    public void setPx(double px) {
        this.px = px;
    }

    public double getPy() {
        return py;
    }

    public void setPy(double py) {
        this.py = py;
    }
}
