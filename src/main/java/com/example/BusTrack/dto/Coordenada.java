package com.example.BusTrack.dto;

public class Coordenada {
    private double px;
    private double py;

    public Coordenada() {}

    public double getPx() { return px; }
    public void setPx(double px) { this.px = px; }

    public double getPy() { return py; }
    public void setPy(double py) { this.py = py; }
}